function OnMouseOver(element) {
    element.classList.add('mouseover');
}

function OnMouseOut(element) {
    element.classList.remove('mouseover');
}

function ButtonMouseOver(element) {

    element.classList.add('button_mouseover');
}

function ButtonMouseOut(element) {
    element.classList.remove('button_mouseover');
}

function OnFocus(element) {
    element.classList.add('focus');
}

function OnBlur(element) {
    element.classList.remove('focus');
}

function ButtonFocus(element) {

    element.classList.add('button_focus');
}

function ButtonBlur(element) {
    element.classList.remove('button_focus');
}
